#' Supplementary distributions and two random number generators.
#' 
#' 
#' The package includes ten distributions supplementing those built into \R:
#' 
#' * [dinvGauss()] The inverse Gaussian and Wald distributions
#' * [dKruskalWallis()] Kruskall-Wallis distribution
#' * [dKendall()] Kendall's Tau
#' * [dFriedman()] Friedman's chi squared
#' * [dSpearman()] Spearman's rho
#' * [dmaxFratio()] The maximum F-ratio distribution
#' * [dPearson()] Pearson product moment correlation coefficient
#' * [dJohnson()] Johnson distributions
#' * [dNormScore()] normal scores
#' * [dghyper()] Generalized hypergeometric distributions (Kemp and Kemp)
#' 
#' In addition, two random number generators of George Marsaglia:
#' 
#' * [rMWC1019()] A very long period pseudo-random generator
#' * [rziggurat()] The Ziggurat normal and exponential generator
#' 
#' @name SuppDists-package
#' @aliases SuppDists
#' @docType package
#' @useDynLib SuppDists
